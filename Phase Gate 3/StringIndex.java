public class StringIndex {

	public static String index(String leet) {	
		String result = " ";
		int length = leet.length();
	for(int count = 0; count < leet.length(); count++) {
		if (leet.charAt() != leet.charAt(length)){ 
		result = leet.charAt();
	}
	}
		return result;
}


	public static void main(String... args) {
		String input = "leetcode";
		System.out.print(index(input));
	

	}
	
}